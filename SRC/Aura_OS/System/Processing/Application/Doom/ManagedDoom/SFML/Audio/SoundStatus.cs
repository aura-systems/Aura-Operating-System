namespace SFML.Audio
{
    ////////////////////////////////////////////////////////////
    /// <summary>
    /// Enumeration of all possible sound states
    /// </summary>
    ////////////////////////////////////////////////////////////
    public enum SoundStatus
    {
        /// <summary>Sound is not playing</summary>
        Stopped,

        /// <summary>Sound is paused</summary>
        Paused,

        /// <summary>Sound is playing</summary>
        Playing
    }
}